def definir_tipo(n):
    if n == '0':
        return "Tren ligero"
    if n == '1':
        return "Metro"
    if n == '2':
        return "Ferrocarril"
    if n == '3':
        return "Bus"
    if n == '4':
        return "Ferry"
    if n == '5':
        return "Tranvia de cable"
    if n == '6':
        return "Ascensor aereo"
    if n == '7':
        return "Funicular"
    if n == '11':
        return "Trolebus"
    if n == '12':
        return "Monorriel"
    